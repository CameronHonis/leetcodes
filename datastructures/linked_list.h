#ifndef LIST_NODE_H
#define LIST_NODE_H

#include <vector>

using namespace std;

struct ListNode {
    int val;
    ListNode *next;
    ListNode() : val(0), next(nullptr) {}
    ListNode(int x) : val(x), next(nullptr) {}
    ListNode(int x, ListNode *next) : val(x), next(next) {}
};

ListNode* linkedListFromVector(vector<int> vec);

#endif // LIST_NODE_H